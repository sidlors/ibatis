package es.indra.hls.indrasid.business.mec.dao.services;

public class Product {
	
	private int id;
	private String name;
	private double price;
	int getId() {
		return id;
	}
	void setId(int id) {
		this.id = id;
	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	double getPrice() {
		return price;
	}
	void setPrice(double price) {
		this.price = price;
	}
	

}
